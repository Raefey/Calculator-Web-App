

class Smartcal:

    @staticmethod
    def addition(arg_1, arg_2):
        return arg_1 + arg_2

    @staticmethod
    def subtract(arg_1, arg_2):
        return arg_1 - arg_2

    @staticmethod
    def multiply(arg_1, arg_2):
        return arg_1 * arg_2

    @staticmethod
    def divide(arg_1, arg_2):
        return arg_1 / arg_2

